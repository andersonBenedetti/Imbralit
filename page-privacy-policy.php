                        integrante da Presidência da República, que possui, dentre outras funções previstas pela LGPD, a
                        de zelar pela proteção dos dados pessoais e por orientar, regulamentar e fiscalizar o
                        cumprimento da legislação;</br>
                        <strong>Controlador</strong>: pessoa natural ou jurídica, de direito público ou privado, a quem
                        competem as
                        decisões referentes ao tratamento de dados pessoais;</br>
                        <strong>Dado pessoal:</strong> informação relacionada a
                        pessoa natural identificada ou identificável, como por exemplo nome, e-mail, telefone, dentre
                        outros;</br>
                        <strong>Dado pessoal sensível:</strong> dado pessoal sobre origem racial ou étnica, convicção
                        religiosa, opinião
                        política, filiação a sindicato ou a organização de caráter religioso, filosófico ou político,
                        dado referente à saúde ou à vida sexual, dado genético ou biométrico, quando vinculado a uma
                        pessoa natural;</br>
                        <strong>Encarregado:</strong> pessoa indicada pelo controlador e operador para atuar como canal
                        de
                        comunicação entre o controlador, os titulares dos dados e a ANPD;</br>
                        <strong>Operador:</strong> pessoa natural ou
                        jurídica, de direito público ou privado, que realiza o Tratamento de dados pessoais em nome do
                        controlador;</br>
                        <strong>Titular:</strong> pessoa natural a quem se referem os dados pessoais que são objeto de
                        tratamento;</br>
                        <strong>Tratamento:</strong> toda operação realizada com dados pessoais, como as que se referem
                        a
                        coleta, produção, recepção, classificação, utilização, acesso, reprodução, transmissão,
                        distribuição, processamento, arquivamento, armazenamento, eliminação, avaliação ou controle da
                        informação, modificação, comunicação, transferência, difusão ou extração;</br>
                        <strong>Usuário:</strong> todo titular
                        que navega no site da Imbralit.
