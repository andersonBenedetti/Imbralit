                        <strong>Livre acesso:</strong> a consulta facilitada à forma que os dados são tratados é
                        garantida;</br>
                        <strong>Qualidade dos dados:</strong> a exatidão e atualização dos dados pessoais é
                        mantida;</br>
                        <strong>Transparência:</strong> as informações sobre o tratamento e agentes responsáveis são
                        claras e acessíveis;</br>
                        <strong>Segurança:</strong> os dados são protegidos por meio de medidas técnicas e
                        administrativas;</br>
                        <strong>Prevenção:</strong> medidas são adotadas para prevenir a ocorrência de danos ao
                        titular;</br>
                        <strong>Não discriminação:</strong> o tratamento não pode ser ilícito ou abusivo;</br>
                        <strong>Responsabilização e prestação de contas:</strong> o agente de tratamento deve demonstrar
